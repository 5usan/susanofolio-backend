import mongoose from "mongoose";

const Schema = mongoose.Schema;

const signupSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    phoneNumber: {
      type: Number,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const signupModel = mongoose.model("signupSchema", signupSchema);

export default signupModel;
